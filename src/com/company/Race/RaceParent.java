package com.company.Race;

public class RaceParent {

    private int abilityScore1;
    private int abilityScore2;
    private String size;
    private int speed;
    private int flySpeed;
    private int swimSpeed;

    public RaceParent(int abilityScore1, int abilityScore2, String size, int speed, int flySpeed, int swimSpeed) {
        this.abilityScore1 = abilityScore1;
        this.abilityScore2 = abilityScore2;
        this.size = size;
        this.speed = speed;
        this.flySpeed = flySpeed;
        this.swimSpeed = swimSpeed;
    }
}
